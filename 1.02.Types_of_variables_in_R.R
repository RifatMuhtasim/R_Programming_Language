### Types of Variables in R ###

# Character classes
colors <- c("red", "green", "blue")
colors
class(colors)

# Factor  classes
colors_f <- factor("red", "green", "blue")
colors_f
class(colors_f)

# Numeric classes
num <- c(1,2,3,4,5)
num
class(num)

# Integer classes
int <- c(1L, 2L, 3L)
int
class(int)

